package com.malkos.poppin.entities;

public class SPSOrderDatePojo {
	private String dateTimeQualifier;
	private String date;
	private String time;
	private String timeCode;
	private String dateTimeFormQualifier;
	private String dateTimePeriod;
	
	
	public String getDateTimeQualifier() {
		return dateTimeQualifier;
	}
	public void setDateTimeQualifier(String dateTimeQualifier) {
		this.dateTimeQualifier = dateTimeQualifier;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getTimeCode() {
		return timeCode;
	}
	public void setTimeCode(String timeCode) {
		this.timeCode = timeCode;
	}
	public String getDateTimeFormQualifier() {
		return dateTimeFormQualifier;
	}
	public void setDateTimeFormQualifier(String dateTimeFormQualifier) {
		this.dateTimeFormQualifier = dateTimeFormQualifier;
	}
	public String getDateTimePeriod() {
		return dateTimePeriod;
	}
	public void setDateTimePeriod(String dateTimePeriod) {
		this.dateTimePeriod = dateTimePeriod;
	}

}
